public class Greatest 
{
    public static void main(String[] args) 
	{
        int A = 10; //data members
        int B = 20;
        int C = 30;

        if (A > B && A > C) 
		{
            System.out.println("A is the greatest");
        }
		else if (B > A && B > C) 
		{
            System.out.println("B is the greatest");
        }
		else
			
		{
            System.out.println("C is the greatest");
        }
    }
}
